// Mostly Used Identifier Cases in JavaScript

// 1. camelCase
//    First word lowercase, subsequent words capitalized, no spaces
//    Used for: Variables, Functions, Methods
//    Example: userName, calculateTotal, getUserData, isActive, maxRetries

// 2. PascalCase
//    All words capitalized, no spaces
//    Used for: Classes, Constructors, Components
//    Example: UserName, CalculateTotal, GetUserData, IsActive, MaxRetries

// 3. CONSTANT_CASE
//    All uppercase, words separated by underscores
//    Used for: Constants
//    Example: USER_NAME, CALCULATE_TOTAL, GET_USER_DATA, IS_ACTIVE, MAX_RETRIES

// 4. snake_case
//    All lowercase, words separated by underscores
//    Used for: Some variables and functions (less common in JavaScript)
//    Example: user_name, calculate_total, get_user_data, is_active, max_retries

// 5. _privateVariable
//    Underscore prefix to indicate private or internal use
//    Used for: Private/internal variables and methods
//    Example: _userName, _calculateTotal, _getUserData, _isActive, _maxRetries

// 6. $specialVariable
//    Dollar sign prefix for special variables
//    Used for: DOM elements or jQuery objects
//    Example: $userName, $calculateTotal, $getUserData, $isActive, $maxRetries
